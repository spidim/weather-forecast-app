export const ElToEnCityName = name => {
	switch (name) {
		case 'Πάτρα':
			return 'Patras'
		case 'Αθήνα':
			return 'Athens'
		case 'Θεσσαλονίκη':
			return 'Thessaloniki'
		case 'Καβάλα':
			return 'Kavala'
		case 'Αλεξανδρούπολη':
			return 'Alexandroupolis'
		case 'Ιωάννινα':
			return 'Ioannina'
		case 'Λάρισα':
			return 'Larissa'
		case 'Καλαμάτα':
			return 'Kalamata'
		case 'Χανιά':
			return 'Chania'
		case 'Ρόδος':
			return 'Rodos'
	}
}
